			<p><b><font color="#333333">Generate Loyalty, Goodwill and Word-of-Mouth</font></b></p>
<p align="justify"> By talking back when they believe they have not received their money's worth, consumers give businesses an opportunity to correct the immediate problem and restore goodwill. Experience shows that consumers who complain about products and services continue to frequent the businesses and buy the products they complain about if they believe the complaint was resolved fairly.</p>
<p align="justify">
Research into complaint behavior reveals that only a fraction of dissatisfied consumers complains to business and, thereby, gives the company an opportunity to correct the problem. There is evidence that some consumers do not complain because they are sceptical about business's willingness or ability to resolve disputes fairly. Consumers simply withdraw their patronage and criticize the company or the product to others.
</p>
<p align="justify">
Such findings underscore the importance to business of complaint management software that is well-publicized and easily accessible. An unregistered complaint may do as much harm as one that is mismanaged or not resolved.
</p>
<p align="justify">
Careful complaint management can save business unwanted costs. For example, negative word-of-mouth publicity from dissatisfied consumers means lost revenue and necessitates additional investment in advertising to attract replacement customers.
</p>
<p align="justify">
Complaints and complaint trends tell business how to do its job better by alerting management to problems that need prompt attention and correction. Furthermore, they indicate long-range opportunities for product innovation and problem prevention. A well-planned system for screening and recording complaint data can provide business owners and managers answers to such important questions as the following:
</p>
<p align="justify">
<li> Are products "oversold" or "over advertised?"</li>
<li>Is advertising clearly understood?</li>
<li>Are salespeople overzealous?</li>
<li>Do product disclosures (such as labelling, warranty information and service agreements) need to be improved?</li>
<li>Are user's manuals clear, complete and easy-to-read?</li>
<li>Would changing warranty coverage reduce complaints?</li>
</p>
<p align="justify">
Complaints also provide information about product quality:
<li>Are there opportunities for product improvements or better quality control?</li>
<li>Are there indications of safety defects that should be reported and corrected, or that justify a recall?</li>
</p>
<p align="justify">To get this valuable feedback, complaint-reporting must generate information swiftly and systematically to the appropriate managers or departments. Initial screening should trigger immediate action, when necessary, and statistical summaries should identify trends and long-range courses of action.
</p>

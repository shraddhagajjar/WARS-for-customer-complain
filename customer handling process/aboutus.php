<p><b><font color="#333333">What Makes a Great Complaint Manager?</font></b></p>

<p align="justify">Complaint managers need to be patient, articulate, and able to balance fairly the interests of the company with those of the consumer. They also should be able to communicate legitimate consumer complaints to management to help determine whether there is a need for changes in company policies or procedures.</p>

<p align="justify">All members of a complaint management department should be familiar with the operations of the company and with its products and services. Prior experience in other departments may be an asset. Training can strengthen interviewing and communications skills and heighten the staff's awareness of the special needs of consumers from different cultural, economic or educational backgrounds. Also, complaint-management staff should be familiar with consumer protection laws and with the operations of third-party dispute-resolution mechanisms to which particularly difficult complaints may need to be referred.
</p>
<p align="justify">
Finally, customer-relations personnel should have professional status, adequate salaries and opportunities for advancement consistent with the importance management assigns to the function.
</p>
